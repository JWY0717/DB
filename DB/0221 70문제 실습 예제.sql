-- 21 <교수> 테이블에서 '김'씨 성을 가진 교수의 자료를 검색하는 SQL문을 작성하시오.
select * 
from 교수 
where 이름 like "김%";

-- 22 <학과> 테이블에서 학과 이름에 '공학'이 포함되어 있는 학과의 정보를 검색하는 SQL문을 작성하시오.
select * 
from 학과 
where 학과명 like "%공학%";

-- 23 <과목> 테이블에서 과목의 영문 이름이 'C'로 시작하여 'e'으로 끝나는 과목의 '과목번호', '과목명', '영문명'을 가져오는 SQL문을 작성하시오.
select 과목번호, 과목명, 영문명 
from 과목 
